      SUBROUTINE USAGE
      CALL ERROR('Usage: pause ([for] <value> [<units>] | until <time>).
     *')
      END
      SUBROUTINE TIMEO0(TIME)
      INTEGER * 4 TIME
      INTEGER TD(5)
      CALL TIMDAT(TD,5)
      TIME=INTL(TD(4))*60+INTL(TD(5))
      RETURN
      END
