      INTEGER FILE(128),LINE(102)
      INTEGER I,FCNT,FD,OUTF(16)
      INTEGER GETARG,CREATE,GETLIN,EQUAL
      INTEGER STD(2)
      INTEGER STD1(3)
      INTEGER STD2(3)
      INTEGER STD3(3)
      DATA STD/173,0/
      DATA STD1/173,177,0/
      DATA STD2/173,178,0/
      DATA STD3/173,179,0/
      FCNT=0
      I=1
      GOTO 10002
10000 I=I+1
10002 IF((GETARG(I,FILE,128).EQ.-1))GOTO 10001
        FCNT=FCNT+1
        IF((EQUAL(FILE,STD).EQ.1))GOTO 10004
        IF((EQUAL(FILE,STD1).EQ.1))GOTO 10004
        GOTO 10003
10004     OUTF(FCNT)=-11
          GOTO 10005
10003     IF((EQUAL(FILE,STD2).NE.1))GOTO 10006
            OUTF(FCNT)=-13
            GOTO 10007
10006       IF((EQUAL(FILE,STD3).NE.1))GOTO 10008
              OUTF(FCNT)=-15
              GOTO 10009
10008         OUTF(FCNT)=CREATE(FILE,2)
              IF((OUTF(FCNT).NE.-3))GOTO 10010
                FCNT=FCNT-1
                CALL PRINT(-15,'*s: can''t create*n.',FILE)
10010       CONTINUE
10009     CONTINUE
10007   CONTINUE
10005 GOTO 10000
10001 CONTINUE
10011 IF((GETLIN(LINE,-10).EQ.-1))GOTO 10012
        CALL PUTLIN(LINE,-11)
        I=1
        GOTO 10015
10013   I=I+1
10015   IF((I.GT.FCNT))GOTO 10014
          CALL PUTLIN(LINE,OUTF(I))
        GOTO 10013
10014 GOTO 10011
10012 GOTO 10018
10016 FCNT=FCNT-1
10018 IF((FCNT.LE.0))GOTO 10017
        FD=OUTF(FCNT)
        IF((FD.EQ.-11))GOTO 10019
        IF((FD.EQ.-13))GOTO 10019
        IF((FD.EQ.-15))GOTO 10019
          CALL CLOSE(FD)
10019 GOTO 10016
10017 CALL SWT
      END
