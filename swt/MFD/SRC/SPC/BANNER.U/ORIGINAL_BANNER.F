      COMMON /LINES/ LINE(132),LINEX(132)
      COMMON /NUMBRS/ I2,I3,I4,I5,I6,I7
      COMMON /INIT0/ NSYMB(2,89),NCHAR(3000)
      DATA NSHIFT/10*0,2,12*0,2,7*0,25,0,25,5*0,25,12,25,7*0,25,
     B     14*0,9,12*0,8,0,0,10,8*0/
