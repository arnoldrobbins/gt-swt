      SUBROUTINE GETBAN (NEGPOS, CARD)
      INTEGER NEGPOS, CARD (78)
C
      INTEGER * 2 GETARG
      INTEGER * 2 ARG (79)
      INTEGER I
C
      IF (GETARG (INTS (1), ARG, INTS (79)) .NE. -1) GOTO 10
         CALL REMARK ('usage:  banner [-] <text>.')
         CALL SWT
10    NEGPOS = +1
      IF (ARG (1) .NE. RS ('-', 24)  .OR.
     &    ARG (2) .NE. -2)                GOTO 20
         NEGPOS = -1
         IF (GETARG (INTS (2), ARG, INTS (79)) .NE. -1) GOTO 20
            ARG (1) = RS ('-', 24)
            ARG (2) = -2
C
20    I = 1
30    IF (ARG (I) .EQ. -2) GOTO 40
         CARD (I) = XOR (LS (INTL (ARG (I)), 24), RS (' ', 8))
         I = I + 1
         GOTO 30
C
40    IF (I .GT. 78)  RETURN
         CARD (I) = ' '
         I = I + 1
         GOTO 40
      END
