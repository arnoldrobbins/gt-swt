      SUBROUTINE LEGU(N)
C                       PRINTS THE INITIAL PART OF UPPER-CASE 'B','D',
C     'E','F','H','I','K','L','M','N','P' AND 'R';    M,N: N=4
C                                                  OTHERS: N=7
