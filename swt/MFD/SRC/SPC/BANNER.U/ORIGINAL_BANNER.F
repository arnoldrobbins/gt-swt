      CALL OUTPUT (LINE)
60    CONTINUE
      GO TO 20
70    IF(I.EQ.8.OR.I.EQ.26.OR.I.EQ.40) CALL OHAV(-1)
      IF(I.EQ.12.OR.I.EQ.16) CALL ACHN
      IF(I.EQ.7.OR.I.EQ.19) CALL OHAF(-1)
      CALL BAXX(2,2)
      RETURN
      END
