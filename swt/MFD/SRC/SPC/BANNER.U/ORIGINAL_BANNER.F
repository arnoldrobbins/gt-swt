      SUBROUTINE OUTPUT (LINE)
      INTEGER LINE (132)
C
      INTEGER * 2 L (134)
      INTEGER I
C
      DO 10 I = 1, 132
         L (I) = RS (LINE (I), 24)
10       CONTINUE
C
      L (133) = :212
      L (134) = -2
      CALL PUTLIN (L, INTS(-11))
      RETURN
      END
