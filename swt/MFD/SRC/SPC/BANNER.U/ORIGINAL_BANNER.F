      NTEMP = NE
      NE = NH
      NH = NTEMP
      NTEMP = NF
      NF = NG
      NG = NTEMP
30    IF(NCHK.EQ.0) NCHK = 1
      IF(NA.NE.0.OR.NB.NE.0) GO TO 40
      CALL BAXX(NCHK,2)
      GO TO 20
40    CALL FRNT(NA-NSI,NB-NSI)
      IF(NC.EQ.0) GO TO 50
      CALL FRNT(NC-NSI,ND-NSI)
      IF(NE.EQ.0) GO TO 50
      CALL FRNT(NE-NSI,NF-NSI)
      IF(NG.EQ.0) GO TO 50
      CALL FRNT(NG-NSI,NH-NSI)
