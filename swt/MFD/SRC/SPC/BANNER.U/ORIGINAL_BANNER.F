      NG = 0
      CALL BAXX(1,1)
      J = J + 1
      IF(J.GE.JOUT) GO TO 70
      NCH = NCHAR(J)
      NA = NCH/I7
      NB = NCH/I5 - NA*I2
      NC = NCH/I3 - NB*I2 - NA*I4
      ND = NCH/10 - NC*I2 - NB*I4 - NA*I6
      NCHK = NCH - ND*10 - NC*I3 - NB*I5 - NA*I7
      IF(NA.LE.NB.AND.NB.LE.NC.AND.NC.LE.ND) GO TO 25
      NTEMP = NA
      NA = ND
      ND = NTEMP
      NTEMP = NB
      NB = NC
      NC = NTEMP
