      RETURN
      END
      SUBROUTINE BAXX(N,J)
C                       PUTS BACKGROUND SYMBOLS IN THE OUTPUT LINES;
C     FOR J.NE.1, PRINTS N LINES OF BACKGROUND.
C
      COMMON /LINES/ LINE(132),LINEX(132)
      COMMON /SYMBOL/ NBLANK,NSYM,NSYMX,LFRONT,LFRNTX,LBACK,LBACKX
      DO 10 I=1,132
      LINE(I) = LBACK
10    LINEX(I) = LBACKX
      IF(J.EQ.1) RETURN
      DO 20 I=1,N
      CALL OUTPUT (LINE)
20    CONTINUE
      RETURN
      END
      SUBROUTINE FRNT(NA,NB)
C                       PUTS FOREGROUND SYMBOLS IN THE OUTPUT LINES
C     FROM NA+MOVE THROUGH NB+MOVE.
C
      COMMON MOVE
      COMMON /LINES/ LINE(132),LINEX(132)
      COMMON /SYMBOL/ NBLANK,NSYM,NSYMX,LFRONT,LFRNTX,LBACK,LBACKX
      I = NA + MOVE
      J = NB + MOVE - 1
      DO 10 K=I,J
      LINEX(K) = LFRNTX
10    LINE(K) = LFRONT
      RETURN
      END
