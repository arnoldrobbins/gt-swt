      NE = NCH/I7
      NF = NCH/I5 - NE*I2
      NG = NCH/I3 - NF*I2 - NE*I4
      NH = NCH/10 - NG*I2 - NF*I4 - NE*I6
      NCHK = NCH - NH*10 - NG*I3 - NF*I5 - NE*I7
